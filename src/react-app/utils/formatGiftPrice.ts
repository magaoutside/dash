/**
 * Форматирует цену подарка согласно требованиям:
 * - Если цена >= 10 и дробная часть .00, показывать без дробной части (10, 20, 1800)
 * - Если цена < 10, показывать с дробной частью (.00)
 * - Для всех остальных случаев показывать как есть
 */
export function formatGiftPrice(price: number): string {
  if (price >= 10 && price % 1 === 0) {
    // Если цена больше или равна 10 и является целым числом
    return price.toString();
  } else if (price < 10) {
    // Если цена меньше 10, всегда показываем с 2 знаками после запятой
    return price.toFixed(2);
  } else {
    // Для всех остальных случаев (например, 15.50)
    return price.toString();
  }
}

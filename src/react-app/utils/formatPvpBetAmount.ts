/**
 * Форматирует сумму ставки в PvP согласно требованиям:
 * - Если сумма больше 1000, показывать без дробной части (1000, 1500)
 * - Если сумма больше 100, показывать с одним знаком после запятой (100.6, 250.5)
 * - Для остальных случаев убирать лишние нули (12.9, 5.5, но не 5.00 -> 5)
 */
export function formatPvpBetAmount(amount: number): string {
  if (amount >= 1000) {
    // Для сумм >= 1000: без дробной части
    return Math.round(amount).toString();
  } else if (amount >= 100) {
    // Для сумм >= 100: один знак после запятой
    return amount.toFixed(1);
  } else {
    // Для остальных: убираем лишние нули
    const formatted = amount.toFixed(2);
    // Убираем лишние нули в конце
    return formatted.replace(/\.?0+$/, '');
  }
}
